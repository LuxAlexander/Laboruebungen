CREATE TABLE MEDIAS (
    media_id            INTEGER NOT NULL,
    title               VARCHAR2(50) NOT NULL,
    description         VARCHAR2(200) NOT NULL,
    age_restriction     INTEGER NOT NULL,
    MEDIA_TYPES_type_id INTEGER NOT NULL,
    CONSTRAINT MEDIAS_PK PRIMARY KEY (media_id),
    CONSTRAINT MEDIAS_MEDIA_TYPES_FK FOREIGN KEY (MEDIA_TYPES_type_id) REFERENCES MEDIA_TYPES (type_id)
);

-- Untertypen von MEDIAS (1:1 Beziehungen)
CREATE TABLE AUDIOS (
    media_id INTEGER NOT NULL,
    codec    VARCHAR2(20) NOT NULL,
    duration DATE NOT NULL,
    CONSTRAINT AUDIOS_PK PRIMARY KEY (media_id),
    CONSTRAINT AUDIOS_MEDIAS_FK FOREIGN KEY (media_id) REFERENCES MEDIAS (media_id)
);

CREATE TABLE BOOKS (
    media_id INTEGER NOT NULL,
    isbn_id  VARCHAR2(50) NOT NULL,
    CONSTRAINT BOOKS_PK PRIMARY KEY (media_id),
    CONSTRAINT BOOKS_MEDIAS_FK FOREIGN KEY (media_id) REFERENCES MEDIAS (media_id)
);

CREATE TABLE MAGAZINES (
    media_id INTEGER NOT NULL,
    edition  VARCHAR2(30),
    CONSTRAINT MAGAZINES_PK PRIMARY KEY (media_id),
    CONSTRAINT MAGAZINES_MEDIAS_FK FOREIGN KEY (media_id) REFERENCES MEDIAS (media_id)
);

CREATE TABLE MOVIES (
    media_id INTEGER NOT NULL,
    format   VARCHAR2(20) NOT NULL,
    duration DATE NOT NULL,
    CONSTRAINT MOVIES_PK PRIMARY KEY (media_id),
    CONSTRAINT MOVIES_MEDIAS_FK FOREIGN KEY (media_id) REFERENCES MEDIAS (media_id)
);

-- Ortsbeziehungen

CREATE TABLE SHELVES (
    shelf_id         VARCHAR2(10) NOT NULL,
    LOCATIONS_loc_id INTEGER NOT NULL,
    shelf_code       VARCHAR2(20),
    CONSTRAINT SHELVES_PK PRIMARY KEY (shelf_id, LOCATIONS_loc_id),
    CONSTRAINT SHELVES_LOCATIONS_FK FOREIGN KEY (LOCATIONS_loc_id) REFERENCES LOCATIONS (loc_id)
);

CREATE TABLE COMPARTMENTS (
    comp_id                  INTEGER NOT NULL,
    SHELVES_shelf_id         VARCHAR2(10) NOT NULL,
    SHELVES_LOCATIONS_loc_id INTEGER NOT NULL,
    position                 VARCHAR2(20),
    CONSTRAINT COMPARTMENTS_PK PRIMARY KEY (comp_id, SHELVES_shelf_id, SHELVES_LOCATIONS_loc_id),
    CONSTRAINT COMPARTMENTS_SHELVES_FK FOREIGN KEY (SHELVES_shelf_id, SHELVES_LOCATIONS_loc_id) 
        REFERENCES SHELVES (shelf_id, LOCATIONS_loc_id)
);

CREATE TABLE COPIES (
    barcode_id            VARCHAR2(15) NOT NULL,
    MEDIAS_media_id       INTEGER NOT NULL,
    COMPARTMENTS_comp_id  INTEGER NOT NULL,
    COMPARTMENTS_shelf_id VARCHAR2(10) NOT NULL,
    COMPARTMENTS_loc_id   INTEGER NOT NULL,
    CONSTRAINT COPIES_PK PRIMARY KEY (barcode_id, MEDIAS_media_id),
    CONSTRAINT COPIES_MEDIAS_FK FOREIGN KEY (MEDIAS_media_id) REFERENCES MEDIAS (media_id),
    CONSTRAINT COPIES_COMPARTMENTS_FK FOREIGN KEY (COMPARTMENTS_comp_id, COMPARTMENTS_shelf_id, COMPARTMENTS_loc_id) 
        REFERENCES COMPARTMENTS (comp_id, SHELVES_shelf_id, SHELVES_LOCATIONS_loc_id)
);

-- 5. VERLEIH & TRANSAKTIONEN

CREATE TABLE LEDGER (
    start_date            DATE NOT NULL,
    end_date              DATE NOT NULL,
    return_dtime          DATE,
    total_cost            NUMBER(9,2),
    cost_paid             NUMBER(9,2),
    CUSTOMERS_customer_id INTEGER NOT NULL,
