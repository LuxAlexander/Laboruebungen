CREATE TABLE STATISTICS (
    statistic_id           INTEGER NOT NULL,
    number_time_borrowed   INTEGER,
    sum_time_borrowed      INTEGER,
    COPIES_barcode_id      VARCHAR2(15) NOT NULL,
    COPIES_MEDIAS_media_id INTEGER NOT NULL,
    CONSTRAINT STATISTICS_PK PRIMARY KEY (statistic_id, COPIES_barcode_id, COPIES_MEDIAS_media_id),
