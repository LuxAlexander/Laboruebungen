    CONSTRAINT LEDGER_CUSTOMERS_FK FOREIGN KEY (CUSTOMERS_customer_id) REFERENCES CUSTOMERS (customer_id),
    CONSTRAINT LEDGER_COPIES_FK FOREIGN KEY (COPIES_barcode_id, COPIES_media_id) 
        REFERENCES COPIES (barcode_id, MEDIAS_media_id)
);

CREATE TABLE RESERVATIONS (
    reservation_id               INTEGER NOT NULL,
    time_reservation             DATE NOT NULL,
    CUSTOMERS_customer_id        INTEGER NOT NULL,
    MEDIAS_media_id              INTEGER NOT NULL,
    status                       VARCHAR2(20),
    LEDGER_CUSTOMERS_customer_id INTEGER NOT NULL,
    LEDGER_COPIES_barcode_id     VARCHAR2(15) NOT NULL,
    LEDGER_COPIES_media_id       INTEGER NOT NULL,
    LEDGER_start_date            DATE NOT NULL,
    CONSTRAINT RESERVATIONS_PK PRIMARY KEY (reservation_id, MEDIAS_media_id, CUSTOMERS_customer_id),
    CONSTRAINT RESERVATIONS_CUSTOMERS_FK FOREIGN KEY (CUSTOMERS_customer_id) REFERENCES CUSTOMERS (customer_id),
    CONSTRAINT RESERVATIONS_MEDIAS_FK FOREIGN KEY (MEDIAS_media_id) REFERENCES MEDIAS (media_id),
    CONSTRAINT RESERVATIONS_LEDGER_FK FOREIGN KEY (LEDGER_CUSTOMERS_customer_id, LEDGER_COPIES_barcode_id, LEDGER_COPIES_media_id, LEDGER_start_date)
        REFERENCES LEDGER (CUSTOMERS_customer_id, COPIES_barcode_id, COPIES_media_id, start_date)
);

