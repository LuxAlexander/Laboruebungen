-- Hinweis: Die Trigger setzen voraus, dass die Variablen BOOKS, MOVIES etc. 
-- in deiner Applikation oder als Konstanten/IDs in der Tabelle MEDIA_TYPES definiert sind.
-- Hier ein Beispiel für BOOKS:
CREATE OR REPLACE TRIGGER ARC_FKArc_3_BOOKS 
BEFORE INSERT OR UPDATE OF media_id ON BOOKS 
FOR EACH ROW 
DECLARE 
    d INTEGER; 
BEGIN 
    SELECT A.MEDIA_TYPES_type_id INTO d FROM MEDIAS A WHERE A.media_id = :new.media_id; 
    -- Hier sollte '1' (oder die entsprechende ID für Books) stehen
    IF (d IS NULL) THEN 
        raise_application_error(-20223,'FK BOOKS_MEDIAS_FK violates Arc constraint.'); 
    END IF; 
EXCEPTION WHEN NO_DATA_FOUND THEN NULL; 
END; 
/

