---- TRIGGER

-- 1. AUTHOR_SEQ -> AUTHORS
CREATE OR REPLACE TRIGGER TRG_AUTHORS_BI
BEFORE INSERT ON AUTHORS FOR EACH ROW
BEGIN
  IF :new.author_id IS NULL THEN
    SELECT AUTHOR_SEQ.NEXTVAL INTO :new.author_id FROM dual;
  END IF;
END;
/

